console.log(null + 3); // отримуємо число, тому що null = 0
console.log(5 && "qwerty"); // отримуємо строку, тому що оператор && виводить останнє значення, якщо всі значення true
console.log(+'40' + +'2' + "hillel"); // отримуємо рядок, тому що робимо конкатенацію строк

console.log('4px' - 3); // отримуємо NaN, тому що від строки пробуємо відняти число
console.log('4' - 3); // отримуємо число, тому що строка '4' при відніманні перетворилася в число
console.log('6' + 3 ** 0); // отримуємо строку, тому що строка '6' додається до одиниці
console.log(12 / '6'); // отримуємо число, строка '6' конвертувалась в число при діленні
console.log('10' + (5 === 6)); // отримуємо строку, булеве значення додалось до строки '10'
console.log(null == ''); // отримуємо булеве значення, тому що йде порівняння
console.log(3 ** (9 / 3)); // отримуємо число, тому що тут операції з числами
console.log(!!'false' == !!'true'); // отримуємо булеве значення, тому що строки конвертувались в булеві значення
console.log(0 || '0' && 1); // отримуємо один, тому що при 0 || 1 отримуємо 1(true)
